package java_day03_my;

public class BitTest {
	public static void main(String[] args) {
		System.out.println(3&5);//AND 연산
		System.out.println(3|5);//OR 연산
		System.out.println(3^5);//XOR 연산
		System.out.println(10 <<2);//1010 <<2 ----> 101000(40)		
		System.out.println(10 >>2);//1010 >>2 ----> 10(2)
		
	}
}
