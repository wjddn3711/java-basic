package java_day02;

public class VariableTest {

	public static void main(String[] args) {
		// 변수 선언하기
		// 자료형 int(정수), double(실수), char(문자), String(문자열)
		// int 타입의 정수 10을 변수에 담기
		int num = 10;
		System.out.println(num);
		
		// 1. double 타입의 실수 3.14를 변수에 저장하기
		//자료형 변수명 = 값;
		//double num = 3.14; 이미 앞에서 선언된 변수는 다시 선언할 수 없다.
		double a = 3.14;
		System.out.println(a);
		
		// 2. char 타입의 문자 'a'를 변수에 저장하기
		char c = 'a';
		System.out.println(c);
		
		// 3. String 타입의 문자열 "안녕하세요"를 변수에 저장하기
		String str = "안녕하세요";
		System.out.println(str);
 		
	}

}













