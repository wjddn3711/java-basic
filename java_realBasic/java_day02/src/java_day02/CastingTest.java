package java_day02;

public class CastingTest {
	public static void main(String[] args) {
		//강제 형 변환
		// (바꾸고싶은자료형)값;
		int num = (int)3.24;
		
		System.out.println((double)num);
		//자동 형변환
		// double 타입으로 형 변환
		double res = 10+2.4;
		System.out.println(res);
		
		// 문자열로 형변환
		String str = ""+1024;
		System.out.println(str);
		
		//문자 
		char c = 'a';
		System.out.println((char)(c+10));
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
